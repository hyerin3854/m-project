package chapter06;

public class MethodEx2 {
	void method1() {
		System.out.println("메소드 시작");
		System.out.println("메소드 끝");
	}
	void method2(int a) {
		System.out.println("메소드2 시작");
		System.out.println("받은 값:"+a);
		System.out.println("메소드2 끝");
	}
	void method3(int a,int b) {
		System.out.println("메소드3 시작");
		System.out.println("받은 값a:"+a);
		System.out.println("받은 값b:"+b);
		System.out.println("메소드3 끝");
	}

}
